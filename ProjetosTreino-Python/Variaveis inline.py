#Para fazer com que leia na mesma linha você somente precisará fazer uma lista vazia recebendo um input.split(" ")
lista = []
lista = input().split(" ")
#A cada espaço será alocada 1 variável,como temos 3 neste exemplo,você poderá escrever um nome,ou colocar números,separando-os por espaço
A = lista[0]
B = lista[1]
C = lista[2]
#primeira variável
print("1:",A)
#segunda
print("2:",B)
#terceira
print("3:",C)
